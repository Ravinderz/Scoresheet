package com.leastcount.model;

import java.util.List;

public class PlayerScoreCard {
	
private String Name;
	
	private int FullCount;
	
	private int Shows;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public int getFullCount() {
		return FullCount;
	}

	public void setFullCount(int fullCount) {
		FullCount = fullCount;
	}

	public int getShows() {
		return Shows;
	}

	public void setShows(int shows) {
		Shows = shows;
	}
	
	

}
